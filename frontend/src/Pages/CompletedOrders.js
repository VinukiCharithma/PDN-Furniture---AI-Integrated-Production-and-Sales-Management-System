const CompletedOrders = () => {
  const [orders, setOrders] = useState([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);
  const [selectedOrder, setSelectedOrder] = useState(null);

  useEffect(() => {
    const fetchOrders = async () => {
      try {
        setLoading(true);
        setError(null);
        const response = await api.get('/tasks/orders');
        setOrders(response.data.filter(order => order.progress === 100));
      } catch (error) {
        console.error("Error fetching completed orders:", error);
        setError("Failed to load completed orders");
      } finally {
        setLoading(false);
      }
    };
    fetchOrders();
  }, []);

  if (loading) return (
    <div className="loading-container">
      <div className="loading-spinner"></div>
      Loading completed orders...
    </div>
  );
  
  if (error) return (
    <div className="error-container">
      <span className="error-icon">‚ö†Ô∏è</span>
      {error}
    </div>
  );

  return (
    <>
      <ProgressNavBar />
      <div className="completed-orders-container">
        <div className="orders-header">
          <h1 className="orders-title">Completed Production</h1>
          <p className="orders-subtitle">Finished manufacturing orders</p>
          <div className="stats-summary">
            <div className="stat-item">
              <span className="stat-value">{orders.length}</span>
              <span className="stat-label">Completed Orders</span>
            </div>
          </div>
        </div>

        {selectedOrder ? (
          <div className="report-view">
            <div className="report-header">
              <h2 className="report-title">
                <span className="back-button" onClick={() => setSelectedOrder(null)}>
                  &larr;
                </span>
                Order #{selectedOrder.orderId} Report
              </h2>
              
              <div className="summary-cards">
                <div className="summary-card">
                  <span className="summary-label">Total Time</span>
                  <span className="summary-value">
                    {selectedOrder.totalEstimatedTime} hours
                  </span>
                </div>
                <div className="summary-card">
                  <span className="summary-label">Priority</span>
                  <span className={`priority-badge ${selectedOrder.priorityLevel.toLowerCase()}`}>
                    {selectedOrder.priorityLevel}
                  </span>
                </div>
                <div className="summary-card">
                  <span className="summary-label">Risk Level</span>
                  <span className="summary-value">
                    {selectedOrder.riskLevel}
                  </span>
                </div>
              </div>
            </div>

            <div className="tasks-table-container">
              <table className="tasks-table">
                <thead>
                  <tr>
                    <th>Task Name</th>
                    <th>Status</th>
                    <th>Time (hrs)</th>
                    <th>Due Date</th>
                  </tr>
                </thead>
                <tbody>
                  {selectedOrder.tasks?.map((task, index) => (
                    <tr key={index}>
                      <td>{task.taskName}</td>
                      <td>
                        <span className={`status-badge ${task.status.toLowerCase().replace(' ', '-')}`}>
                          {task.status}
                        </span>
                      </td>
                      <td>{task.estimatedTime}</td>
                      <td>
                        {task.dueDate ? new Date(task.dueDate).toLocaleDateString() : 'N/A'}
                      </td>
                    </tr>
                  ))}
                </tbody>
              </table>
            </div>

            <div className="report-actions">
              <PDFDownloadLink
                document={<ProductionReportPDF order={selectedOrder} />}
                fileName={`luxury_furniture_report_${selectedOrder.orderId}.pdf`}
                className="pdf-button"
              >
                {({ loading }) => (
                  loading ? 'Preparing Report...' : 'Download PDF Report'
                )}
              </PDFDownloadLink>
            </div>
          </div>
        ) : (
          <div className="orders-grid-container">
            {orders.length === 0 ? (
              <div className="empty-state">
                <div className="empty-icon">üéâ</div>
                <h3 className="empty-title">No Completed Orders</h3>
                <p className="empty-text">All completed orders will appear here</p>
              </div>
            ) : (
              <div className="orders-grid">
                {orders.map(order => (
                  <div key={order._id} className="order-card">
                    <div className="card-header">
                      <span className="order-id">Order #{order.orderId}</span>
                      <span className={`priority-badge ${order.priorityLevel.toLowerCase()}`}>
                        {order.priorityLevel}
                      </span>
                    </div>
                    <div className="card-body">
                      <div className="meta-item">
                        <span className="meta-label">Production Time:</span>
                        <span className="meta-value">{order.totalEstimatedTime} hours</span>
                      </div>
                      <div className="meta-item">
                        <span className="meta-label">Risk Level:</span>
                        <span className="meta-value">{order.riskLevel}</span>
                      </div>
                    </div>
                    <div className="card-footer">
                      <button
                        className="view-button"
                        onClick={() => setSelectedOrder(order)}
                      >
                        View Full Report
                      </button>
                    </div>
                  </div>
                ))}
              </div>
            )}
          </div>
        )}
      </div>
    </>
  );
};

export default CompletedOrders;